import { strict as assert } from 'node:assert';
import { createRequire } from 'node:module';
import fs from 'node:fs/promises';
import net from 'node:net';
import path from 'node:path';
import { startVulnerableApp } from './fixtures/vulnerable-app/server.mjs';

const require = createRequire(import.meta.url);
const { CallbackListenerService } = require('../dist-electron/callbackListenerService.js');
const { CertificateAuthorityManager } = require('../dist-electron/certManager.js');
const { ProjectStore, ProjectStoreCallbackRecorder } = require('../dist-electron/projectStore.js');
const {
  ProxyEngine,
  buildIntruderOastCorrelationPackage,
  correlateIntruderOastResults,
} = require('../dist-electron/proxyEngine.js');

const artifactRoot = path.resolve('.gitignored/test-artifacts/intruder-oast-correlation', `${Date.now()}-${process.pid}`);
const projectDir = path.join(artifactRoot, 'intruder-oast.proxyforge');
await fs.mkdir(artifactRoot, { recursive: true });

const app = await startVulnerableApp();
let callbackService;
let callbackRecorder;
let reopened;

try {
  const callbackPort = await freePort();
  const payloads = Array.from({ length: 3 }, (_unused, index) => ({
    id: `cb-intruder-oast-${index}`,
    token: `pf-intruder-oast-${index}`,
    label: `Intruder OAST payload ${index}`,
    protocol: 'http',
    endpoint: `http://127.0.0.1:${callbackPort}/probe?token=pf-intruder-oast-${index}`,
    createdAt: `2026-05-25T22:40:0${index}.000Z`,
    status: 'waiting',
    sourceExchangeId: 'hx-intruder-oast-source',
    sourceHost: new URL(app.httpUrl).host,
    sourcePath: '/ssrf',
    notes: 'Generated by the Intruder OAST row-correlation regression.',
  }));

  const seedStore = await ProjectStore.create(projectDir, {
    projectName: 'Intruder OAST Correlation Workflow',
    projectId: 'intruder-oast-correlation',
  });
  await seedStore.addHttpExchange({
    id: 'hx-intruder-oast-source',
    method: 'GET',
    url: `${app.httpUrl}/ssrf`,
    host: new URL(app.httpUrl).host,
    path: '/ssrf',
    status: 200,
    source: 'repeater',
    tags: ['intruder-source', 'oast', 'ssrf-candidate'],
    notes: 'Source request used to generate Intruder OAST payload rows.',
    requestRaw: [
      'GET /ssrf?url=§callback§ HTTP/1.1',
      `Host: ${new URL(app.httpUrl).host}`,
      'Authorization: Bearer intruder-oast-source-token',
      'Cookie: session=intruder-oast-source-cookie',
      '',
      '',
    ].join('\r\n'),
    responseRaw: 'HTTP/1.1 200 OK\r\nContent-Type: application/json\r\n\r\n{"source":true}',
  });
  seedStore.close();

  callbackRecorder = new ProjectStoreCallbackRecorder(projectDir, {
    projectName: 'Intruder OAST Correlation Workflow',
    projectId: 'intruder-oast-correlation',
  });
  callbackService = new CallbackListenerService({
    payloads: (_profile, activePayloads) => callbackRecorder.capturePayloads(activePayloads),
    interaction: (_profile, interaction) => callbackRecorder.captureInteraction(interaction),
  });
  const profile = {
    id: 'intruder-oast-runtime',
    name: 'Intruder OAST runtime',
    createdAt: '2026-05-25T22:40:00.000Z',
    mode: 'local-http',
    status: 'planned',
    host: '127.0.0.1',
    publicBaseUrl: 'oast.local',
    protocols: ['http'],
    httpPort: callbackPort,
    dnsPort: 0,
    smtpPort: 0,
    pollIntervalSeconds: 2,
    retentionHours: 24,
    signingKeyId: 'intruder-oast-key',
    ciCommand: 'proxyforge oast poll --listener intruder-oast-runtime',
    healthChecks: [],
    summary: 'Intruder OAST callback regression profile',
    content: '{}',
  };
  const listenerStatus = await callbackService.start(profile, payloads);
  await callbackRecorder.flush();
  assert.equal(listenerStatus.running, true);
  assert.equal(listenerStatus.ports.http, callbackPort);

  const proxy = new ProxyEngine(
    () => undefined,
    new CertificateAuthorityManager(path.join(artifactRoot, 'certs')),
  );
  const rawRequest = [
    'GET /ssrf?url=§callback§ HTTP/1.1',
    `Host: ${new URL(app.httpUrl).host}`,
    'Authorization: Bearer intruder-oast-secret-token',
    'Cookie: session=intruder-oast-cookie',
    'X-API-Key: intruder-oast-api-key',
    'Accept: application/json',
    '',
    '',
  ].join('\r\n');
  const summary = await proxy.runIntruderAttack({
    rawRequest,
    targetUrl: `${app.httpUrl}/ssrf?url=seed`,
    payloads: payloads.map((payload) => encodeURIComponent(payload.endpoint)),
    scopeAllowlist: ['127.0.0.1'],
    throttleMs: 0,
    grepTerms: ['callbackStatus'],
    oastPayloads: payloads,
  });

  assert.equal(summary.blocked, false);
  assert.equal(summary.totalRequests, 3);
  assert.equal(summary.oast.payloadCount, 3);
  assert.equal(summary.results.length, 3);
  assert.equal(summary.results.every((result) => result.oastPayloadIds.length === 1), true);
  assert.equal(summary.results.every((result) => result.tags.includes('oast-payload')), true);
  assert.equal(app.state.ssrfCalls.length, 3);
  assert.deepEqual(app.state.ssrfCalls.map((call) => call.callbackUrl), payloads.map((payload) => payload.endpoint));

  await delay(100);
  const poll = callbackService.poll(profile.id, payloads);
  assert.equal(poll.newInteractionIds.length, 3);
  assert.equal(poll.interactions.length, 3);
  assert.deepEqual(new Set(poll.interactions.map((interaction) => interaction.payloadId)), new Set(payloads.map((payload) => payload.id)));

  const correlated = correlateIntruderOastResults(summary, payloads, poll.interactions);
  assert.equal(correlated.oast.correlatedResultCount, 3);
  assert.equal(correlated.oast.correlatedInteractionCount, 3);
  assert.deepEqual(correlated.oast.pendingPayloadIds, []);
  assert.equal(correlated.results.every((result) => result.callbackInteractionIds.length === 1), true);
  assert.equal(correlated.results.every((result) => result.tags.includes('oast-hit')), true);

  const packageResult = buildIntruderOastCorrelationPackage({
    summary,
    payloads,
    interactions: poll.interactions,
    generatedAt: '2026-05-25T22:45:00.000Z',
  });
  assert.equal(packageResult.kind, 'proxyforge-intruder-oast-correlation-package');
  assert.equal(packageResult.requirements.payloadRowsCovered, true);
  assert.equal(packageResult.requirements.callbackInteractionsCovered, true);
  assert.equal(packageResult.requirements.rowCorrelationCovered, true);
  assert.equal(packageResult.requirements.rawExecutorMaterialPreserved, true);
  assert.equal(packageResult.reportRedactionBoundary, 'redact-only-during-report-export');
  assert.match(packageResult.content, /Bearer intruder-oast-secret-token|session=intruder-oast-cookie|intruder-oast-api-key|pf-intruder-oast-2/);
  await fs.writeFile(path.join(artifactRoot, 'intruder-oast-correlation-package.json'), JSON.stringify(packageResult, null, 2), 'utf8');

  await callbackRecorder.flush();
  await callbackService.stopAll();
  await callbackRecorder.close();

  reopened = await ProjectStore.open(projectDir);
  const stats = reopened.stats();
  assert.equal(stats.exchangeCount, 1);
  assert.equal(stats.callbackPayloadCount, 3);
  assert.equal(stats.callbackInteractionCount, 3);
  for (const payload of payloads) {
    const rows = reopened.listCallbackInteractions({ payloadId: payload.id, limit: 5 });
    assert.equal(rows.length, 1);
    assert.match(reopened.getCallbackInteraction(rows[0].id).raw.toString('utf8'), new RegExp(payload.token));
  }

  console.log(`intruder-oast-correlation: mapped ${packageResult.correlatedInteractionCount} callback interaction(s) to ${packageResult.correlatedResultCount} Intruder result row(s) in ${projectDir}`);
} finally {
  reopened?.close();
  await callbackService?.stopAll().catch(() => undefined);
  await callbackRecorder?.close().catch(() => undefined);
  await app.close().catch(() => undefined);
}

function freePort() {
  return new Promise((resolve, reject) => {
    const server = net.createServer();
    server.once('error', reject);
    server.listen(0, '127.0.0.1', () => {
      const address = server.address();
      const port = typeof address === 'object' && address ? address.port : 0;
      server.close((error) => {
        if (error) reject(error);
        else resolve(port);
      });
    });
  });
}

function delay(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}
